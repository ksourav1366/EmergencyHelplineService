                            <h4>Laws And Rights Every person should be aware of </h4>
                            <!--<span>Posted by <a href="#" class="tran3s p-color">admin</a>  at 04 Feb, 2017</span>-->
                        </div> <!-- /.post-heading -->
                        <p>There is nothing more empowering than knowing your country's laws and rights. You never know when you might need to put them into use. While, most of us are aware of some basic laws and rights, here are some of them which we might not know about:</p>
                        <br />
                        
                       <p><strong>1. If your cylinder explodes you are entitled to Rs. 40 lakh cover!</strong></p>
                        <br  />
                        
                           <p>Many of us are unaware that domestic LPG consumers are entitled to a cover of Rs. 40 lakh in case of loss of life or damage to property due to cylinder explosions.</p>
                        <br />
                          <p><strong>2. Penalised for receiving gifts? Yes, they can be bribes.</strong></p>
                        <br  />
                        
                           <p>It has become a tradition for companies to send gifts! As these gifts can be carefully veiled bribes, the law is a smart move by the government to avoid situations of bribery. Know more about The Foreign Contribution Regulation Act 2010' </p>
                        <br />
                          <p><strong>3. Only female officers can escort women to the police station</strong></p>
                        <br  />
                        
                           <p>Not only do male officers have no right to escort a woman but she can refuse to go to the police station between 6 pm to 6 am. In case of a serious crime, a written permit from the magistrate is required for male officers to escort her.</p>
                        <br />

                           <p><strong>4. The tax recovery officer can arrest and release you</strong></p>
                        <br  />
                        
                           <p>In case of tax violations, the TRO has the right to arrest you, though a summon has to be sent. The tax commissioner only decides how long can you be in custody, but your release will be decided by the TRO. This has been mentioned in the Income-tax Act, 1961 </p>
                        <br />

                           <p><strong>5. No traffic violation laws for non-motorised vehicles</strong></p>
                        <br  />
                        
                           <p>Though it has been clarified that a golf cart is not allowed on the road but there aren't any penalties against non motor vehicles like a cycles or rickshaws, since they don't fall under the Motor Vehicles Act.</p>
                        <br />
                           <p><strong>6. Women can lodge complaints through emails</strong></p>
                        <br  />
                        
                           <p>Guidelines issued by the Delhi Police entitle women to the privilege of registering a complaint via email or even through post if she can't go to the police station.</p>
                        <br />
                           <p><strong>7. Live-in relationships are not illegal</strong></p>
                        <br  />
                        
                           <p>Though it is frowned upon in our country, but as long as both the adults are ready to stay together, live-in relationships are not illegal. Moreover, live-in relationships are considered to be 'equal to marriage' if certain conditions are met few of which are pooling of financial and domestic arrangements, entrusting the responsibility, sexual relationship, bearing children, socialization in public and intention, conduct of the parties, etc. This is done to protect women under the Domestic Violence Act . Also, children born out of live-in relationships have the right to inherit their parent's property.</p>
                        <br />
                           <p><strong>8. Political parties can solicit your vehicle during elections</strong></p>
                        <br  />
                        
                           <p>During the time of elections, a political party can solicit your car or bike for campaigning purposes after deciding a settlement with you. Also, parties cannot offer free rides to and fro from poll booths.</p>
                           <p><strong>9. If you have been fined once in the day you maybe excused after that</strong></p>
                        <br  />
                        
                           <p>Riding around without a helmet can land you into trouble, but once you are fined for it, the chalaan slip can get you out of being fined for the same till midnight. Yes, but this is not an encouragement to do so. Be safe while driving.</p>
                        <br />
                           <p><strong>10. You don't have to pay the MRP, you can buy for less</strong></p>
                        <br  />
                        
                           <p>MRP is the Maximum Retail Price. As consumers, you have the right to bargain for a price below that. However, a seller can not go beyond the MRP.</p>
                        <br />
                           <p><strong>11. If you aren't paid, file a complaint immediately</strong></p>
                        <br  />
                        
                           <p>The Limitation Act states that if you aren't paid by someone who is contracted to pay you, you need to file a complaint within 3 years. After that time period your suit most likely will be dismissed, so being lazy about this is not an option.</p>
                        <br />
                           <p><strong>12. 3 months in jail for PDA! (Public Display of Affection)</strong></p>
                        <br  />
                        
                           <p>PDA within its limits is allowed but any obscene activity is punishable by law for upto 3 months. Since the word obscene has not been defined, couples are often harassed by policemen.</p>
                        <br />
                           <p><strong>13. Head constables have limited prosecution power</strong></p>
                        <br  />
                        
                           <p>No head constable can fine you for any offence which presently has a penalty for more than Rs.100. But violation of more than one rule can result in a challan of higher value. This was issued by the Delhi Traffic Police and information about the same is available on their Facebook page.</p>
                        <br />
                           <p><strong>14. A police officer is always on duty, literally</strong></p>
                        <br  />
                        
                           <p>The 1861 Police Act clearly states that a police officers is always on duty. If he or she witnesses an act of crime or if an incident is brought to their knowledge they can't say "I am not on duty" even if they aren't in uniform. Of course, they are entitled to some rest because of the the hard work that they put in to their work.</p>
                        <br />
                           <p><strong>15. The no sex divorce</strong></p>
                        <br  />
                        
                           <p>As funny as it sounds, if a husband or a wife refuse sex post marriage, it can be deemed as 'mental cruelty' and is a viable reason for divorce.</p>
                        <br />
                           <p><strong>16. If you have a son and you are looking to adopt another... well, you can't!</strong></p>
                        <br  />
                        
                           <p>The Hindu Adoptions and Maintenance Act, 1956 clearly states that if you have a Hindu son or a son's son or a son's son's son, you cannot adopt another one. Same goes for a daughter. And the minimum age gap should be 21 years between the child and the adopter.</p>
