                        <p>
                            This quick primer on common basic first aid procedures can help get you through a minor crisis, at least until the paramedics arrive or you can get to medical treatment.
                            These tips are based on the 2010 first aid procedures recommended by the American Heart Association and American Red Cross.
                            They are not a substitute for proper first aid training but can be an introduction to what you can do.
                        </p>
                        <br />
                        <p>
                            You don’t want to go searching for supplies when there’s a minor accident to tend to. Your kit should be easy to find.
                            But it should be kept in a high, childproof cabinet, far away from kids’ prying fingers.
                            Keep one full-sized kit in a central spot at home, such as your kitchen or bathroom.
                            Then put a smaller kit in your car or purse for when you’re on the road.
                        </p>
