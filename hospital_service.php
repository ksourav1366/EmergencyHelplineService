                        <!-- Basic First Aid for Cardiac Arrest -->

                        <h6>Basic First Aid for Cardiac Arrest</h6>
                        <p><strong>The steps to take when a cardiac arrest is suspected are:</strong></p>


                        <p><strong>Step 1</strong></p>
                        <p>Command someone to call 911 or the medical alert system for the locale.</p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            Immediately start chest compressions regardless of your training.
                            Compress hard and fast in the center of the chest, allowing recoil between compressions.
                            Hand this task over to those who are trained if and when they arrive.
                        </p>
                        <br />
                        <p><strong>Step 3</strong></p>
                        <p>If you are trained, use chest compressions and rescue breathing.</p>
                        <br />
                        <p><strong>Step 4</strong></p>
                        <p>>An AED should be applied and used. But it is essential not to delay chest compressions, so finding one should be commanded to someone else while you are doing chest compressions.</p>
                        <br />


                        <h6>Basic First Aid for Bleeding</h6>
                        <p><strong>There are steps to take if you are faced with bleeding right now.</strong></p>

                        <p><strong>Step 1</strong></p>
                        <p>
                            Cover the wound with a gauze or a cloth and apply direct pressure to stop the blood flow.
                            Don't remove the cloth. Add more layers if needed. The cloth will help clots form to stop the flow.
                        </p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            In most cases, applying a tourniquet may do more damage to the limb than good.
                            The 2010 American Heart Association guidelines also discount the value of elevation and using pressure points.
                        </p>
                        <br />


                        <!-- Basic First Aid for Burns -->

                        <h6>Basic First Aid for Burns</h6>
                        <p><strong>Take these first aid steps:</strong></p>
                        <p><strong>Step 1</strong></p>
                        <p>Flush the burned area with cool running water for several minutes. Do not use ice.</p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            Apply a light gauze bandage.
                        </p>
                        <br />
                        <p><strong>Step 3</strong></p>
                        <p> Do not apply ointments, butter, or oily remedies to the burn.</p>
                        <br />
                        <p><strong>Step 4</strong></p>
                        <p> Take ibuprofen or acetaminophen for pain relief if necessary.</p>
                        <br />
                        <p><strong>Step 5</strong></p>
                        <p>   Do not break any blisters that may have formed.</p>
                        <br />


                        <!-- Basic First Aid for Blisters -->
                        <h6>Basic First Aid for Blisters</h6>
                        <p><strong>Take these first aid steps:</strong></p><br>
                        <p>
                            Whether or not a blister needs any treatment is debatable. If the blister is small, unbroken and not very painful,
                            it is probably best to leave it alone. Cover it to prevent continued rubbing and pressure on it that can cause it to swell more and possibly burst on its own.
                        </p>

                        <p>
                            If the blister is large or painful – especially if the activity isn’t finished (such as you are in the middle of a hike) –
                            follow steps to drain and dress a blister. Use a sterilized needle and make small punctures at the edge of the blister and express the fluid.
                            Then apply antibiotic ointment and cover it to protect it from further rubbing and pressure.
                        </p>

                        <br>
                        <!-- Basic First Aid for Fracture -->
                        <h6>Basic First Aid for Fractures</h6>
                        <p><strong>Take these steps for a suspected fracture:</strong></p>
                        <p><strong>Step 1</strong></p>
                        <p> Don't try to straighten it.</p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            Stabilize the limb using a splint and padding to keep it immobile.
                        </p>
                        <br />
                        <p><strong>Step 3</strong></p>
                        <p>
                            Put a cold pack on the injury, avoiding placing ice directly on the skin.
                        </p>
                        <br />
                        <p><strong>Step 4</strong></p>
                        <p>
                            Elevate the extremity.
                        </p>
                        <br />
                        <p><strong>Step 5</strong></p>
                        <p>
                            Give anti-inflammatory drugs like ibuprofen or naproxen.
                        </p>
                        <br />

                        <!-- Basic First Aid for Nosebleeds-->
                        <h6>Basic First Aid for Nosebleeds</h6>
                        <p><strong>The first aid for nosebleed includes:</strong></p>


                        <p><strong>Step 1</strong></p>
                        <p>Lean forward, not back.</p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            Pinch the nose just below the bridge. Don't pinch the nostrils closed by pinching lower.
                        </p>
                        <br />
                        <p><strong>Step 3</strong></p>
                        <p>

                            Check after five minutes to see if bleeding has stopped. If not, continue pinching and check after another 10 minutes.
                        </p>
                        <br />
                        <p><strong>Step 4</strong></p>
                        <p> You can also apply a cold pack to the bridge of the nose while pinching.</p>
                        <br />

                        <!-- Basic First Aid for Bee Stings-->
                        <h6>
                            Basic First Aid for Bee Stings
                        </h6>
                        <p><strong>Use these bee sting first aid tips:</strong></p>
                        <p><strong>Step 1</strong></p>
                        <p>              Get the stinger out any way you can to prevent more venom being delivered. It's a myth that any particular way is better or worse.</p>
                        <br />
                        <p><strong>Step 2</strong></p>
                        <p>
                            If the person is known to be allergic to bee stings, use an EpiPen to prevent anaphylaxis or call 911 if none is available.
                        </p>
                        <br />
                        <p><strong>Step 3</strong></p>
                        <p>

                            Use a cold pack to reduce swelling at the site, but take care not to cause frostbite.
                        </p>
                        <br />
                        <p><strong>Step 4</strong></p>
                        <p>Use an antihistamine like Benadryl (diphenhydramine) to reduce swelling and itching.</p>
                        <br />

                        <p><strong>Step 5</strong></p>
                        <p>Try ibuprofen or Tylenol (acetaminophen) for pain.</p>
                        <br />

                        <p><strong>Step 6</strong></p>
                        <p>Monitor the person who was stung by signs of anaphylaxis, including hives, redness or itching in other areas of the body, and shortness of breath.</p>
