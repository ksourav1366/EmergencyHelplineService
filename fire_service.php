                        <!-- Types of Fires --> 
                        
                        <p><strong>Class A</strong></p>
                        <p><img src="images/blog/class-a.png" alt="Image">Class A fires are fires in ordinary combustibles such as wood, paper, cloth, rubber, and many plastics.</p>
                        <br />
                        <p><strong>Class B</strong></p>
                        <p><img src="images/blog/class-b.png" alt="Image">Class B fires are fires in flammable liquids such as gasoline, petroleum greases, tars, oils, oil-based paints, solvents, alcohols. Class B fires also include flammable gases such as propane and butane. Class B fires do not include fires involving cooking oils and grease.</p>
                        <br />
                        <p><strong>Class C</strong></p>
                        <p><img src="images/blog/class-c.png" alt="Image">Class C fires are fires involving energized electical equipment such as computers, servers, motors, transformers, and appliances. Remove the power and the Class C fire becomes one of the other classes of fire.</p>
                        <br />
                        <p><strong>Class D</strong></p>
                        <p><img src="images/blog/class-d.png" alt="Image">Class D fires are fires in combustible metals such as magnesium, titanium, zirconium, sodium, lithium, and potassium.</p>
                        <br />
                        

                        <h6>EXTINCTION OF FIRE</h6>
                         <ul style="list-style-type:disc;">
                            <li><strong>COOLING:</strong>Reduce the temperature below to the ignition temperature.</li>
                            <li><strong>SMOTHERING:</strong>To cut off the source of oxygen from atmospheric air.</li>
                            <li><strong>STARVATION:</strong>Removal of fuel(combustible materials) from combustion zone.</li>
                         </ul>
                         <!-- Types of Fire Extinguishers --> 

                        <h6>Types of Fire Extinguishers</h6>
                        <p><strong>Water and Foam</strong></p>
                        <p><img src="images/blog/water-and-foam.jpg" alt="Image">Water and Foam fire extinguishers extinguish the fire by taking away the heat element of the fire triangle. Foam agents also separate the oxygen element from the other elements.
                            <br />Water extinguishers are for Class A fires only - they should not be used on Class B or C fires. The discharge stream could spread the flammable liquid in a Class B fire or could create a shock hazard on a Class C fire.</p>
                        <br />
                        <p><strong>Carbon Dioxide</strong></p>
                        <p><img src="images/blog/carbon-dioxide.jpg" alt="Image">Carbon Dioxide fire extinguishers extinguish fire by taking away the oxygen element of the fire triangle and also be removing the heat with a very cold discharge.
                            <br />Carbon dioxide can be used on Class B & C fires. They are usually ineffective on Class A fires.</p>
                        <br />
                        <p><strong>Dry Chemical</strong></p>
                        <p><img src="images/blog/dry-chemical.jpg" alt="Image">Dry Chemical fire extinguishers extinguish the fire primarily by interrupting the chemical reaction of the fire triangle.
                            <br />Today's most widely used type of fire extinguisher is the multipurpose dry chemical that is effective on Class A, B, and C fires. This agent also works by creating a barrier between the oxygen element and the fuel element on Class A fires.
                            <br />Ordinary dry chemical is for Class B & C fires only. It is important to use the correct extinguisher for the type of fuel! Using the incorrect agent can allow the fire to re-ignite after apparently being extinguished succesfully.</p>
                        <br />
                        <p><strong>Wet Chemical</strong></p>
                        <p><img src="images/blog/wet-chemical.jpg" alt="Image">Wet Chemical is a new agent that extinguishes the fire by removing the heat of the fire triangle and prevents re-ignition by creating a barrier between the oxygen and fuel elements.
                            <br />Wet chemical of Class K extinguishers were developed for modern, high efficiency deep fat fryers in commercial cooking operations. Some may also be used on Class A fires in commercial kitchens.</p>
                        <br />
                        <p><strong>Clean Agent</strong></p>
                        <p><img src="images/blog/clean-agent.jpg" alt="Image">Halogenated or Clean Agent extinguishers include the halon agents as well as the newer and less ozone depleting halocarbon agents. They extinguish the fire by interrupting the chemical reaction and/or removing heat from the fire triangle.
                            <br />Clean agent extinguishers are effective on Class A, B and C fires. Smaller sized handheld extinguishers are not large enough to obtain a 1A rating and may carry only a Class B and C rating.</p>
                        <br />
                        <p><strong>Dry Powder</strong></p>
                        <p><img src="images/blog/dry-powder.jpg" alt="Image">Dry Powder extinguishers are similar to dry chemical except that they extinguish the fire by separating the fuel from the oxygen element or by removing the heat element of the fire triangle.
                            <br />However, dry powder extinguishers are for Class D or combustible metal fires, only. They are ineffective on all other classes of fires.</p>
                        <br />
                        <p><strong>Water Mist</strong></p>
                        <p><img src="images/blog/water-mist.jpg" alt="Image">Water Mist extinguishers are a recent development that extinguish the fire by taking away the heat element of the fire triangle. They are an alternative to the clean agent extinguishers where contamination is a concern.
                            <br />Water mist extinguishers are primarily for Class A fires, although they are safe for use on Class C fires as well.</p>
                        <br />
                        <p><strong>Cartridge Operated Dry Chemical</strong></p>
                        <p><img src="images/blog/cartridge-operated.jpg" alt="Image">Cartridge Operated Dry Chemical fire extinguishers extinguish the fire primarily by interrupting the chemical reaction of the fire triangle.
                            <br />Like the stored pressure dry chemical extinguishers, the multipurpose dry chemical is effective on Class A, B, and C fires. This agent also works by creating a barrier between the oxygen element and the fuel element on Class A fires.
                            <br />Ordinary dry chemical is for Class B & C fires only. It is important to use the correct extinguisher for the type of fuel! Using the incorrect agent can allow the fire to re-ignite after apparently being extinguished successfully.</p>
                        <br />
                       
                         <!-- Fire Extinguisher Use -->
                        <h6>Fire Extinguisher Use</h6>
                        <p><strong>It is important to know the locations and the types of extinguishers in your workplace prior to actually using one.</strong></p>
                        <p>Fire extinguishers can be heavy, so it's a good idea to practice picking up and holding an extinguisher to get an idea of the weight and feel.
                            Take time to read the operating instructions and warnings found on the fire extinguisher label. Not all fire extinguishers look alike.
                            Practice releasing the discharge hose or horn and aiming it at the base of an imagined fire. Do not pull the pin or squeeze the lever. This will break the extinguisher seal and cause it to lose pressure.</p>
                        <br />
                        <p><strong>When it is time to use the extinguisher on a fire, just remember PASS!</strong></p>
                        <br />
                        <p><b>Pull</b> the pin.
                           <br /><b>Aim</b> the nozzle or hose at the base of the fire from the recommended safe distance.
                           <br /><b>Squeeze</b> the operating lever to discharge the fire extinguishing agent.
                           <br />Starting at the recommended distance, <b>Sweep</b> the nozzle or hose from side to side until the fire is out. Move forward or around the fire area as the fire diminishes. Watch the area in case of re-ignition.</p>

