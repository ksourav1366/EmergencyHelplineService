                        <p>Fire safety is the set of practices intended to reduce the destruction caused by fire. Fire safety measures include those that are intended to prevent ignition of an uncontrolled fire, and those that are used to limit the development and effects of a fire after it starts.</p>
                        <br />
                        <p>Fire safety measures include those that are planned during the construction of a building or implemented in structures that are already standing, and those that are taught to occupants of the building.</p>
                        <br />
                        <p>Threats to fire safety are commonly referred to as fire hazards. A fire hazard may include a situation that increases the likelihood of a fire or may impede escape in the event a fire occurs.</p>
                        <br />
                        <p>Fire safety is often a component of building safety. Those who inspect buildings for violations of the Fire Code and go into schools to educate children on Fire Safety topics are fire department members known as Fire Prevention Officers. The Chief Fire Prevention Officer or Chief of Fire Prevention will normally train newcomers to the Fire Prevention Division and may also conduct inspections or make presentations.</p>
                        
                        <h6>Elements of a fire safety policy</h6>
                        <p>Fire safety policies apply at the construction of a building and throughout its operating life. Building codes are enacted by local, sub-national, or national governments to ensure such features as adequate fire exits, signage, and construction details such as fire stops and fire rated doors, windows, and walls. Fire safety is also an objective of electrical codes to prevent overheating of wiring or equipment, and to protect from ignition by electrical faults.</p>
                        <br />
                        <p>Fire codes regulate such requirements as the maximum occupancy for buildings such as theatres or restaurants, for example. Fire codes may require portable fire extinguishers within a building, or may require permanently installed fire detection and suppression equipment such as a fire sprinkler system and a fire alarm system.</p>
                        <br />
                        <p>Local authorities charged with fire safety may conduct regular inspections for such items as usable fire exits and proper exit signage, functional fire extinguishers of the correct type in accessible places, and proper storage and handling of flammable materials. Depending on local regulations, a fire inspection may result in a notice of required action, or closing of a building until it can be put into compliance with fire code requirements.</p>
                        <br />
                        <p>Owners and managers of a building may implement additional fire policies. For example, an industrial site may designate and train particular employees as a fire fighting force. Managers must ensure buildings comply with fire evacuation regulations, and that building features such as spray fireproofing remains undamaged. Fire policies may be in place to dictate training and awareness of occupants and users of the building to avoid obvious mistakes, such as the propping open of fire doors. Buildings, especially institutions such as schools, may conduct fire drills at regular intervals throughout the year.</p>
                       
